package net.citizensnpcs.api.compat;

/**
 * Represents a position in an {@link World}.
 */
public class Position {
    private final World world;
    private final double x;
    private final double y;
    private final double z;
    private final float yaw;
    private final float pitch;

    public Position(World world, double x, double y, double z) {
        this(world, x, y, z, 0, 0);
    }

    public Position(World world, double x, double y, double z, float yaw, float pitch) {
        this.world = world;
        this.x = x;
        this.y = y;
        this.z = z;
        this.yaw = yaw;
        this.pitch = pitch;
    }
}
